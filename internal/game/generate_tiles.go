// generateTiles randomly generates 106 tiles in random order
func generateTiles() [][2]int {
	newTiles := tiles[:]

	rand.Shuffle(106, func(i, j int) {
		newTiles[i], newTiles[j] = newTiles[j], newTiles[i]
	})

	return newTiles
}
